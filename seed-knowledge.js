async function seedData() {
  const sql = await initSqlJs();

  if (!fs.existsSync(DB_PATH)) {
    console.error('数据库文件不存在:', DB_PATH);
    console.error('请先启动一次服务器以创建数据库');
    process.exit(1);
  }

  const fileBuffer = fs.readFileSync(DB_PATH);
  const db = new sql.Database(fileBuffer);

  try {
    // 查找管理员用户ID
    const adminResult = db.exec("SELECT id, username FROM users WHERE role = 'admin' LIMIT 1");
    if (adminResult.length === 0 || adminResult[0].values.length === 0) {
      console.error('未找到管理员用户');
      db.close();
      process.exit(1);
    }
    const adminId = adminResult[0].values[0][0];
    const adminName = adminResult[0].values[0][1];
    console.log(`使用管理员账号: ${adminName} (ID: ${adminId})`);

    // 检查是否已存在数据
    const existing = db.exec('SELECT COUNT(*) FROM knowledge_base');
    const existingCount = existing[0].values[0][0];
    if (existingCount > 0) {
      console.log(`资料库已有 ${existingCount} 条数据，跳过导入。`);
      console.log('如需重新导入，请先清空 knowledge_base 表。');
      db.close();
      process.exit(0);
    }

    let inserted = 0;
    for (const item of sampleData) {
      db.run(
        'INSERT INTO knowledge_base (title, content, category, tags, created_by) VALUES (?, ?, ?, ?, ?)',
        [item.title, item.content.trim(), item.category, item.tags, adminId]
      );
      inserted++;
      console.log(`  ✓ 已导入: ${item.title} [${item.category}]`);
    }

    // 保存数据库
    const data = db.export();
    fs.writeFileSync(DB_PATH, Buffer.from(data));

    console.log(`\n✅ 成功导入 ${inserted} 条业务资料！`);
    console.log('请重启服务器以加载新数据。');
  } catch (err) {
    console.error('导入失败:', err.message);
  } finally {
    db.close();
  }
}

seedData();
